    private void PtambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PtambahActionPerformed

        try {
            product pdt = new product();
            category ct = new category();
            pdt.setProduct_name(PNama.getText());
            pdt.setProduct_desc(PDeskripsi.getText());
            pdt.setProduct_price(Integer.parseInt(PHarga.getText()));
            ct.setCategory_name(PKategori.getSelectedItem().toString());
            ResultSet data = ct.Konversi();

            if (data.next()) {
                String isi = data.getString("category_id");
                pdt.setProduct_cat_id(Integer.parseInt(isi));
            }
            pdt.tambahProduct();
            loadTabel();
            reset();
        } catch (SQLException sQLException) {
        }

    }//GEN-LAST:event_PtambahActionPerformed

