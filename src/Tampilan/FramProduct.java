    void loadTabel() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Deskripsi");
        model.addColumn("Harga");
        model.addColumn("Category");

        try {
            product pd = new product();
            ResultSet data = pd.tampilProduct();

            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString("product_id"),
                    data.getString("product_name"),
                    data.getString("product_desc"),
                    data.getInt("product_price"),
                    data.getString("category_name")
                });
            }
        } catch (SQLException sQLException) {
        }
        PProduc.setModel(model);
    }

    void comboCategory() {
        try {
            category cat = new category();
            ResultSet data = cat.tampilComboBox();

            while (data.next()) {
                String isi = data.getString("category_name");
                PKategori.addItem(isi);
            }

        } catch (SQLException ex) {

        }
    }

    void otoID() {
        try {
            product prod = new product();
            ResultSet id = prod.otoID();

            if (id.next()) {
                int auto = id.getInt("product_id") + 1;
                Pid.setText(String.valueOf(auto));

            } else {
                Pid.setText("1");
            }
            Pid.setEditable(false);

        } catch (SQLException sQLException) {
        }

    }

    void reset() {
        otoID();
        Pid.setEditable(true);
        PNama.setText(null);
        PHarga.setText(null);
        PDeskripsi.setText(null);
        PKategori.setSelectedItem(null);
    }

